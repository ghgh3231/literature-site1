const state = {
  currentCategory: null,
  currentPoem: null,
  searchQuery: "",
  authorFilter: ""
};

const elements = {
  categoriesScreen: document.getElementById("categoriesScreen"),
  poemsScreen: document.getElementById("poemsScreen"),
  poemScreen: document.getElementById("poemScreen"),
  categoriesGrid: document.getElementById("categoriesGrid"),
  poemsGrid: document.getElementById("poemsGrid"),
  poemsTitle: document.getElementById("poemsTitle"),
  poemTitle: document.getElementById("poemTitle"),
  poemAuthor: document.getElementById("poemAuthor"),
  poemCode: document.getElementById("poemCode"),
  poemCategory: document.getElementById("poemCategory"),
  poemText: document.getElementById("poemText"),
  backToCategories: document.getElementById("backToCategories"),
  backToPoems: document.getElementById("backToPoems"),
  breadcrumbs: document.getElementById("breadcrumbs"),
  crumbHome: document.getElementById("crumbHome"),
  crumbCategory: document.getElementById("crumbCategory"),
  crumbPoem: document.getElementById("crumbPoem"),
  crumbSeparator2: document.getElementById("crumbSeparator2"),
  statsPoems: document.getElementById("statsPoems"),
  statsAuthors: document.getElementById("statsAuthors"),
  searchInput: document.getElementById("searchInput"),
  authorFilter: document.getElementById("authorFilter"),
  resultsSummary: document.getElementById("resultsSummary")
};

function init() {
  elements.statsPoems.textContent = poetryData.length;
  elements.statsAuthors.textContent = new Set(poetryData.map((item) => item.author)).size;
  renderCategories();
  bindEvents();
  initRevealObserver();
}

function bindEvents() {
  elements.backToCategories.addEventListener("click", showCategoriesScreen);
  elements.backToPoems.addEventListener("click", showPoemsScreen);
  elements.crumbHome.addEventListener("click", showCategoriesScreen);
  elements.crumbCategory.addEventListener("click", () => {
    if (state.currentCategory) showPoemsScreen();
  });

  elements.searchInput.addEventListener("input", (event) => {
    state.searchQuery = event.target.value.trim().toLowerCase();
    renderCurrentCategoryPoems();
  });

  elements.authorFilter.addEventListener("change", (event) => {
    state.authorFilter = event.target.value;
    renderCurrentCategoryPoems();
  });
}

function renderCategories() {
  elements.categoriesGrid.innerHTML = "";

  categories.forEach((category, index) => {
    const count = poetryData.filter((item) => item.category === category.id).length;

    const card = document.createElement("button");
    card.className = "category-card";
    card.type = "button";
    card.setAttribute("aria-label", `Открыть категорию ${category.title}`);

    card.innerHTML = `
      <div class="card-top">
        <div class="card-icon"><i class="fa-solid ${category.icon}"></i></div>
        <div class="card-index">0${index + 1}</div>
      </div>
      <h3>${category.title}</h3>
      <p>${category.description}</p>
      <div class="card-footer">
        <span class="mini-tag">${category.accent}</span>
        <div class="count-tag">${count} произвед.</div>
        <div class="card-arrow"><i class="fa-solid fa-arrow-right"></i></div>
      </div>
    `;
    addMouseGlow(card);
    card.addEventListener("click", () => openCategory(category.id));
    elements.categoriesGrid.appendChild(card);
  });
}

function openCategory(categoryName) {
  state.currentCategory = categoryName;
  state.currentPoem = null;
  state.searchQuery = "";
  state.authorFilter = "";

  elements.poemsTitle.textContent = categoryName;
  elements.searchInput.value = "";
  populateAuthorFilter(categoryName);
  renderCurrentCategoryPoems();

  switchScreen(elements.poemsScreen);
  updateBreadcrumbs();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function populateAuthorFilter(categoryName) {
  const authors = [...new Set(
    poetryData
      .filter((item) => item.category === categoryName)
      .map((item) => item.author)
  )].sort((a, b) => a.localeCompare(b, "ru"));

  elements.authorFilter.innerHTML = `<option value="">Все авторы</option>`;
  authors.forEach((author) => {
    const option = document.createElement("option");
    option.value = author;
    option.textContent = author;
    elements.authorFilter.appendChild(option);
  });
}

function getFilteredPoems() {
  return poetryData.filter((item) => {
    const byCategory = item.category === state.currentCategory;
    const byAuthor = !state.authorFilter || item.author === state.authorFilter;
    const query = state.searchQuery;
    const haystack = `${item.title} ${item.author} ${item.text}`.toLowerCase();
    const byQuery = !query || haystack.includes(query);
    return byCategory && byAuthor && byQuery;
  });
}

function renderCurrentCategoryPoems() {
  const poems = getFilteredPoems();
  renderPoems(poems);
  elements.resultsSummary.textContent = `Показано: ${poems.length}`;
}

function renderPoems(poems) {
  elements.poemsGrid.innerHTML = "";

  if (!poems.length) {
    elements.poemsGrid.innerHTML = `
      <div class="empty-state">
        <h3>Ничего не найдено</h3>
        <p>Попробуйте изменить запрос или выбрать другого автора.</p>
      </div>
    `;
    return;
  }

  poems.forEach((poem) => {
    const excerpt = createExcerpt(poem.text, 160);
    const card = document.createElement("button");
    card.className = "poem-card";
    card.type = "button";
    card.setAttribute("aria-label", `Открыть стихотворение ${poem.title}`);

    card.innerHTML = `
      <div class="poem-card-top">
        <div>
          <h3>${poem.title}</h3>
          <div class="poem-author">${poem.author}</div>
        </div>
        <div class="card-arrow"><i class="fa-solid fa-book-open"></i></div>
      </div>
      <p class="poem-excerpt">${excerpt}</p>
      <div class="poem-meta-line">
        <span class="meta-badge accent"><i class="fa-solid fa-hashtag"></i>${poem.code}</span>
        <span class="meta-badge"><i class="fa-solid fa-layer-group"></i>${poem.category}</span>
      </div>
    `;
    addMouseGlow(card);
    card.addEventListener("click", () => openPoem(poem.id));
    elements.poemsGrid.appendChild(card);
  });
}

function openPoem(poemId) {
  const poem = poetryData.find((item) => item.id === poemId);
  if (!poem) return;

  state.currentPoem = poem;
  elements.poemTitle.textContent = poem.title;
  elements.poemAuthor.textContent = poem.author;
  elements.poemCode.textContent = poem.code;
  elements.poemCategory.textContent = poem.category;
  elements.poemText.textContent = poem.text;

  switchScreen(elements.poemScreen);
  updateBreadcrumbs();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function showCategoriesScreen() {
  state.currentCategory = null;
  state.currentPoem = null;
  switchScreen(elements.categoriesScreen);
  updateBreadcrumbs();
}

function showPoemsScreen() {
  if (!state.currentCategory) {
    showCategoriesScreen();
    return;
  }
  switchScreen(elements.poemsScreen);
  updateBreadcrumbs();
}

function switchScreen(targetScreen) {
  [elements.categoriesScreen, elements.poemsScreen, elements.poemScreen].forEach((screen) => {
    if (screen === targetScreen) {
      screen.classList.remove("hidden");
    } else {
      screen.classList.add("hidden");
    }
  });
}

function updateBreadcrumbs() {
  const hasCategory = Boolean(state.currentCategory);
  const hasPoem = Boolean(state.currentPoem);

  elements.breadcrumbs.classList.toggle("hidden", !hasCategory && !hasPoem);

  if (hasCategory) {
    elements.crumbCategory.classList.remove("hidden");
    elements.crumbCategory.textContent = state.currentCategory;
  } else {
    elements.crumbCategory.classList.add("hidden");
  }

  if (hasPoem) {
    elements.crumbPoem.classList.remove("hidden");
    elements.crumbPoem.textContent = state.currentPoem.title;
    elements.crumbSeparator2.classList.remove("hidden");
  } else {
    elements.crumbPoem.classList.add("hidden");
    elements.crumbSeparator2.classList.add("hidden");
  }
}

function createExcerpt(text, limit = 140) {
  const cleanText = text.replace(/\s+/g, " ").trim();
  if (cleanText.length <= limit) return cleanText;
  return `${cleanText.slice(0, limit).trim()}…`;
}

function addMouseGlow(element) {
  element.addEventListener("mousemove", (event) => {
    const rect = element.getBoundingClientRect();
    const x = event.clientX - rect.left;
    const y = event.clientY - rect.top;
    element.style.setProperty("--mx", `${x}px`);
    element.style.setProperty("--my", `${y}px`);
  });
}

function initRevealObserver() {
  const revealItems = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("reveal-visible");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.18 });
  revealItems.forEach((item) => observer.observe(item));
}

